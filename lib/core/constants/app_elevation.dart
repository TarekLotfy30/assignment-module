abstract final class AppElevation {
  /// Elevation for card components
  /// Creates subtle shadow for content separation
  static const double xSmallElevation = 2;

  /// Elevation for FloatingActionButton
  /// Prominent shadow for primary action button
  static const double fabElevation = 6;

  /// Elevation for regular buttons
  /// Moderate shadow for interactive elements
  static const double buttonElevation = 2;

  /// Elevation for app bar
  /// Minimal shadow when scrolled
  static const double appBarElevation = 0;
  static const double appBarScrolledUnderElevation = 1;

  /// Elevation for bottom navigation
  /// Clear separation from content
  static const double bottomNavigationElevation = 8;

  /// Elevation for dialogs
  /// High elevation for modal content
  static const double dialogElevation = 24;

  /// Elevation for snack bars
  /// Above most content but below dialogs
  static const double snackBarElevation = 6;
}
